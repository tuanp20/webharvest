let currentLocale = localStorage.getItem('locale') || 'vi';

function t(key, params = {}) {
    const localeDict = translations[currentLocale] || translations['vi'];
    let text = localeDict[key] || key;
    
    // Interpolating dynamic properties
    Object.keys(params).forEach(paramKey => {
        text = text.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), params[paramKey]);
    });
    
    return text;
}

// Walk HTML structure and update strings for matched keys
function translateDOM() {
    // 1. Text elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        el.textContent = t(key);
    });
    
    // 2. Placeholder text
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        el.setAttribute('placeholder', t(key));
    });
    
    // 3. Elements with title tooltips
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        el.setAttribute('title', t(key));
    });
    
    // 4. Alt attributes
    document.querySelectorAll('[data-i18n-alt]').forEach(el => {
        const key = el.getAttribute('data-i18n-alt');
        el.setAttribute('alt', t(key));
    });
}

function changeLanguage(lang) {
    if (translations[lang]) {
        currentLocale = lang;
        localStorage.setItem('locale', lang);
        document.documentElement.setAttribute('lang', lang);
        translateDOM();
        
        // Sync language selector dropdowns if multiple exist
        const selectors = document.querySelectorAll('#language-select');
        selectors.forEach(select => {
            if (select.value !== lang) {
                select.value = lang;
            }
        });

        // Dispatch a global event for dynamic parts
        window.dispatchEvent(new CustomEvent('languageChanged', { detail: { locale: lang } }));
    }
}

// Run translation immediately on DOM load
document.addEventListener('DOMContentLoaded', () => {
    document.documentElement.setAttribute('lang', currentLocale);
    
    // Bind select element if present
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = currentLocale;
        langSelect.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }

    translateDOM();
});
